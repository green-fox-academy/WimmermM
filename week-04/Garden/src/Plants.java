public class Plants {

    int waterLevel;
    String color;
    boolean needWater;
    String type;



    public Plants(){
    }


    public Plants(int waterLevel, String color, boolean needWater, String type) {
        this.waterLevel = waterLevel;
        this.color = color;
        this.needWater = needWater;
        this.type = type;
    }
}
